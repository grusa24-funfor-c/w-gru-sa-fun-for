<h2>Somos...</h2>
		<p>Una organización de profesionales y estudiantes en ciencias de la salud, educación, comunicación social y otras áreas afines a la actividad del grupo.  Juntos formamos un equipo humano multi- e interdisciplinario, comprometidos por llegar a distintas comunidades en materia de prevención y promoción de la salud, llevándoles las Buenas Nuevas, sin importar sus condiciones fí­sicas, intelectuales o socioculturales. </p>
	<br />
</td>

<td>
	<h2>Así comenzó todo...</h2>
	<p>Con la idea expuesta de formar un grupo de asistencia en salud, formulada por el pastor Boris Masalyka, que se encontraba en el corazón de Dios, ponemos en marcha, el 20 de enero del 2006, un grupo interdisciplinario en el área de salud y educación con el propósito de servir a la comunidad, desde otro punto de vista las necesidades básicas insatisfechas. Así fue que algunos de nosotros, que luego ya aumentamos en numero, nos comenzamos a reunir, planeando una táctica de trabajo de servicio como proyecto, que fue presentado al Ministerio de Salud de la provincia de Córdoba, Argentina, dirección de epidemiología, a la directora Doctora Maria Frias, Pediatra y Epidemiologa, quien lo avalo, pasando a ser autorizado por la provincia de Córdoba, con el nombre "Jóvenes Por La Comunidad - Grupo Salud".<br /> Actualmente Grupo Salud constituye un programa de <strong>Fundación Forjar</strong></p>
</td>
